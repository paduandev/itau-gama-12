package exemplos;
public class Exemplo03 {
    public static void main(String[] args) {
        int x; // cria uma variável do tipo número inteiro

        x = 23; // guarda o valor 23 dentro da variável

        System.out.println( x );

        System.out.println( x + 2 );

        System.out.println( x );

        x = x + 2; // altera o valor da variável com o resultado da conta

        System.out.println( x );

        System.out.println( "O valor final = " + x ); 
    }
}
